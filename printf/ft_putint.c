#include "ft_printf.h"

int	ft_putint(int n)
{
	int	len;

	len = 0;
	if (n == -2147483648)
	{
		len += ft_putstr("-2147483648");
		return (len);
	}
	if (n < 0)
	{
		len += ft_putchar('-');
		n *= -1;
	}
	len += ft_putnbr_base(n, "0123456789");
	return (len);
}
